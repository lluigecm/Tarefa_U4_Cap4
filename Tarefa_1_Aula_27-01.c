const Matriz_leds_config *nums[] = {&zero, &one, &two, &three, &four, &five, &six, &seven, &eight, &nine};

void init_gpios() {
    gpio_init(LED_PIN);
    gpio_set_dir(LED_PIN, GPIO_OUT);

    gpio_init(BUTTON_A);
    gpio_set_dir(BUTTON_A, GPIO_IN);
    gpio_pull_up(BUTTON_A); // pull-up resistor

    gpio_init(BUTTON_B);
    gpio_set_dir(BUTTON_B, GPIO_IN);
    gpio_pull_up(BUTTON_B); // pull-up resistor
}

void blink_led() {
    gpio_put(LED_PIN, 1);
    sleep_ms(100);
    gpio_put(LED_PIN, 0);
    sleep_ms(100);
}

int main()
{
    stdio_init_all();
    init_gpios();

    int i = 0;
    PIO pio = pio0; 
    uint sm = configurar_matriz(pio);

    imprimir_desenho(nums[i], pio, sm);   

    while (true) {
        blink_led();

       if(gpio_get(BUTTON_A) == 0){
            sleep_ms(50);   // Debounce
            
            if(gpio_get(BUTTON_A) == 0 && i <= 8){
                i++;
                imprimir_desenho(nums[i], pio, sm);
            }
        }

        if(gpio_get(BUTTON_B) == 0){
            sleep_ms(50);   // Debounce

            if(gpio_get(BUTTON_B) == 0 && i >= 1){
                i--;
                imprimir_desenho(nums[i], pio, sm);
            }
        }
    }
}
