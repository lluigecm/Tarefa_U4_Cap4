const Matriz_leds_config *nums[] = {&zero, &one, &two, &three, &four, &five, &six, &seven, &eight, &nine};

void init_gpios() {
    gpio_init(LED_PIN);
    gpio_set_dir(LED_PIN, GPIO_OUT);

    gpio_init(BUTTON_A);
    gpio_set_dir(BUTTON_A, GPIO_IN);
    gpio_pull_up(BUTTON_A); // pull-up resistor

    gpio_init(BUTTON_B);
    gpio_set_dir(BUTTON_B, GPIO_IN);
    gpio_pull_up(BUTTON_B); // pull-up resistor
}

void blink_led() {
    gpio_put(LED_PIN, 1);
    sleep_ms(100);
    gpio_put(LED_PIN, 0);
    sleep_ms(100);
}

void button_callback(uint gpio, uint32_t events) {
   
   if(to_ms_since_boot(get_absolute_time()) - lastTime > 200){  // Debounce
        lastTime = to_ms_since_boot(get_absolute_time());

        if(gpio == BUTTON_A){
            button_a_pressed = true;
        }
        else if(gpio == BUTTON_B){
            button_b_pressed = true;
        }
    }
}


int main() {

    stdio_init_all();
    init_gpios();

    int i = 0;
    PIO pio = pio0;
    uint sm = configurar_matriz(pio);

    imprimir_desenho(nums[i], pio, sm);

    gpio_set_irq_enabled_with_callback(BUTTON_A, GPIO_IRQ_EDGE_FALL, true, &button_callback);
    gpio_set_irq_enabled_with_callback(BUTTON_B, GPIO_IRQ_EDGE_FALL, true, &button_callback);

    while(true){
        blink_led();

        if(button_a_pressed){
            button_a_pressed = false;

            if(i < 9){
                i++;
            }
            imprimir_desenho(nums[i], pio, sm);
        }
        
        if(button_b_pressed){
            button_b_pressed = false;

            if(i > 0){
                i--;
            }

            imprimir_desenho(nums[i], pio, sm);
        }
    }
    return 0;
}